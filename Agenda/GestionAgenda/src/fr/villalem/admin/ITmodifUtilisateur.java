    private String[] getNomPrenom(String txt){
        String[] nom = new String[2];
        nom[0] = "";
        nom[1] = "";
        int place = 0;
        for(int i=0 ; i<txt.length() ; i++){
            if(txt.charAt(i) == ' '){
                place = 1;
            }
            else{
                nom[place] += txt.charAt(i);
            }
        }
        return nom;
    }

